export function getPlacesByMood(moodId) {
  if (!moodId) return places
  return places.filter((p) => p.moodMatches.includes(moodId))
}

export function getPlaceById(id) {
  return places.find((p) => p.id === Number(id))
}

export function getMoodById(id) {
  return moods.find((m) => m.id === id)
}

